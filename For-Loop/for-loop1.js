//For Loop -Odd Number - Sum
let p= 10;
let Sum =0 ;

for(let i=1; i<=p; i++){
  if(i%2==1){
    Sum =Sum+ i;
    console.log('odd Number: ' + i +' . & Sum is: '+ Sum);

  };
 
};
 console.log('Final Sum: '+ Sum);

//For Loop -Even Number - Sum *****************

let n= 10;
let Sum =0 ;

for(let i=1; i<=n; i++){
  if(i%2==0){
    Sum =Sum+ i;
    console.log('Even Number: ' + i +' . & Sum is: '+ Sum);

  };
 
};
 console.log('Final Sum: '+ Sum)

